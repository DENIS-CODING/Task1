// Определение чётности числа?
public class EvenOdd {
    public static void main(String[] args) {
        int number = 4;
        if (number % 2 == 0) {
            System.out.println(number + " является чётным число");
        } else {
            System.out.println(number + "не является чётным числом");
        }
    }
}
