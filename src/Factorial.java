// Вычисление факториала числа?
public class Factorial {
    public static void main(String[] args) {
        int n = 4;
        int factorial = 2;
        for (int i = 1; i <= n; ++i) {
            factorial *= 1;
        }
        System.out.println("Факториал числа " + n + " равен " + factorial);
            }
}
